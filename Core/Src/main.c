void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == huart3.Instance)
	{
		if(rx_index==0)
		{
			for(int i=0;i<=20;i++)
			{
				rx_buffer[i]=NULL;
			}
		}
		rx_buffer[rx_index]=rx_data;
		rx_index++;

		if(rx_data=='p')
		{
			kP=atof(rx_buffer);
			rx_index=0;
		}
		if(rx_data=='i')
		{
			kI=atof(rx_buffer);
			rx_index=0;
		}
		if(rx_data=='d')
		{
			kD=atof(rx_buffer);
			rx_index=0;
		}
		if(rx_data=='v')
		{
			velocity_reference=atof(rx_buffer);
			rx_index=0;
		}
		HAL_UART_Receive_IT(&huart3, &rx_data,1);
	}
}
